// <summary>
// This class contains the http response from an http request.
// </summary>


// <summary>
// Constructor.
// </summary>
function HttpResponse ()
{
	// <summary>
	// Response status code.
	// </summary>
	this.StatusCode;
	
	
	// <summary>
	// Response text.
	// </summary>
	this.ResponseText;
	
	
	// <summary>
	// Optional state object.
	// </summary>
	this.State;
}